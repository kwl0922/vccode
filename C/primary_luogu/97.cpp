#include<stdio.h>

int main(){
    int n,m,i,j;
    scanf("%d%d",&n,&m);
    char a[n][m];
    for(i=0;i<n;i++){
        for(j=0;j<m;j++){
            scanf(" %c",&a[i][j]);
        }
    }
    for(i=0;i<n;i++){
        for(j=0;j<m;j++){
            if(a[i][j]=='?'){
                a[i][j]='0';
            }
        }
    }
    for(i=0;i<n;i++){
        for(j=0;j<m;j++){
            if(a[i][j]=='*'){
                if(j-1>=0&&a[i][j-1]!='*'){//w
                    a[i][j-1]=((a[i][j-1]-'0')+1)+'0';
                }
                if(j+1<=m-1&&a[i][j+1]!='*'){//e
                    a[i][j+1]=((a[i][j+1]-'0')+1)+'0';
                }
                if(i-1>=0&&a[i-1][j]!='*'){//n
                    a[i-1][j]=((a[i-1][j]-'0')+1)+'0';
                }
                if(i+1<=n-1&&a[i+1][j]!='*'){//s
                    a[i+1][j]=((a[i+1][j]-'0')+1)+'0';
                }
                if(i-1>=0&&j-1>=0&&a[i-1][j-1]!='*'){//nw
                    a[i-1][j-1]=((a[i-1][j-1]-'0')+1)+'0';
                }
                if(i-1>=0&&j+1<=m-1&&a[i-1][j+1]!='*'){//ne
                    a[i-1][j+1]=((a[i-1][j+1]-'0')+1)+'0';
                }
                if(i+1<=n-1&&j-1>=0&&a[i+1][j-1]!='*'){//sw
                    a[i+1][j-1]=((a[i+1][j-1]-'0')+1)+'0';
                }
                if(i+1<=n-1&&j+1<=m-1&&a[i+1][j+1]!='*'){//se
                    a[i+1][j+1]=((a[i+1][j+1]-'0')+1)+'0';
                }
            }
        }
    }
    for(i=0;i<n;i++){
        for(j=0;j<m;j++){
            printf("%c",a[i][j]);
        }
        printf("\n");
    }
    return 0;
}